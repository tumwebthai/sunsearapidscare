/** All settings keys for social links */
export const SOCIAL_SETTINGS_KEYS = SOCIAL_META.map((s) => s.settingsKey)

/** Merge Supabase settings with social metadata → returns only links that have a URL */
export function mergeSocialLinks(urls: Record<string, string>): SocialLink[] {
  return SOCIAL_META
    .map((meta) => ({
      ...meta,
      href: urls[meta.settingsKey] || meta.defaultHref,
    }))
    .filter((link) => !!link.href)
}
